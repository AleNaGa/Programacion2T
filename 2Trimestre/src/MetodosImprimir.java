public class MetodosImprimir {
    public static void imprimirArraysInt (int [] array){
        for (int var : array) {
            System.out.print(var + " ");
        }
    }
    public static void imprimirArraysDouble (double [] array){
        for (double var : array) {
            System.out.print(var + " ");
        }
    }
    public static void imprimirArraysChar (char [] array){
        for (char var : array) {
            System.out.print(var + " ");
        }
    }
    public static void imprimirArraysFloat (float [] array){
        for (float var : array) {
            System.out.print(var + " ");
        }
    }
    public static void imprimirArraysBoolean (boolean [] array){
        for (boolean var : array) {
            System.out.print(var + " ");
        }
    }
}
